# 📊 R Project 

This R project uses the Random Forest algorithm to predict F1 driver performance based on simulated data like tyre degradation, track temperature, and fuel level. It showcases basic regression modeling using randomForest and dplyr in R.t.

---

## 🎯 Objective

To analyze and visualize patterns within the dataset using **R programming**, and provide insights that can help in decision-making or further statistical modeling.

---

## 🚀 Features

- Data cleaning and preprocessing
- Exploratory Data Analysis (EDA)
- Data visualization using `ggplot2` and `plotly`
- Summary statistics and correlation analysis
- Use of functions and scripting in R
- Report generation

---

## 🛠️ Technologies Used

- **R Language**
- **RStudio**
- Libraries:
  - `dplyr`
  - `ggplot2`
  - `tidyr`
  - `readr`
  - `plotly`
  - `corrplot`

